def pytest_sessionstart(session: pytest.Session) -> None:
    """Seed MongoDB with minimal test data before tests run.

    IMPORTANT: This hook does NOT generate embeddings to avoid loading the
    SentenceTransformer model at the master process level, which causes
    PyTorch meta tensor errors with pytest-xdist.

    Embeddings are generated by function-scoped fixtures in worker processes
    AFTER xdist spawns workers. This ensures each worker loads its own
    independent model instance.

    Test data here is minimal - just enough to prevent "no data" errors.
    Actual embedding generation happens in worker fixtures.
    """
    import socket
    from contextlib import closing

    # Check if MongoDB is reachable before attempting to seed
    try:
        with closing(socket.socket(socket.AF_INET, socket.SOCK_STREAM)) as sock:
            sock.settimeout(2)
            result = sock.connect_ex(("localhost", 27018))
            if result != 0:
                return  # MongoDB not available, skip seeding
    except Exception:
        return  # Network issue, skip seeding

    client = None
    try:
        from secondbrain.config import Config
        from secondbrain.storage import VectorStorage

        config = Config()

        # Use direct MongoDB check
        from pymongo import MongoClient as PyMongoClient

        client = PyMongoClient(config.mongo_uri, serverSelectionTimeoutMS=10000, maxPoolSize=50)
        direct_db = client.get_database(config.mongo_db)
        if "embeddings_test" in direct_db.list_collection_names():
            direct_count = direct_db.embeddings.count_documents({})
        else:
            direct_count = 0

        if direct_count > 0:
            return  # Data already seeded

        # Seed with minimal document metadata only - NO embeddings
        # Actual embeddings will be generated by worker fixtures when needed
        test_documents = [
            {
                "chunk_id": "test-chunk-001",
                "chunk_text": "Test document for testing. This is placeholder text.",
                "source_file": "/test/docs/test.md",
                "file_type": "markdown",
                "metadata": {"title": "Test Document", "page": 1, "test": True},
                # Embedding will be generated by worker fixture, not here
            },
        ]

        # Insert without embeddings - workers will generate them
        collection = direct_db.get_collection("embeddings_test")
        
        if test_documents:
            collection.insert_many(test_documents)
            print(f"Seeded {len(test_documents)} test documents (embeddings generated by worker fixtures)")

    except Exception as e:
        print(f"Warning: Failed to seed test data: {e}")
    finally:
        if client is not None:
            client.close()
