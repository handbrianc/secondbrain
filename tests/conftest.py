from typing import Any


@pytest.fixture(scope="session")
def embedding_cache() -> Any:
    """Session-scoped embedding cache to prevent repeated generation.

    Caches embeddings by text content to avoid regenerating the same embeddings
    across multiple tests. This provides significant speedup for test suites
    that query the same texts repeatedly.

    Usage:
        def test_something(embedding_cache, embed_gen):
            embedding = embedding_cache("some text", embed_gen)
    """
    cache: dict[str, list[float]] = {}

    def get_or_create(text: str, embed_gen: Any) -> list[float]:
        if text not in cache:
            cache[text] = embed_gen.generate(text)
        return cache[text]

    return get_or_create


